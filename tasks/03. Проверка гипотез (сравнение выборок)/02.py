import numpy as np
import scipy.stats as stats

# данные из таблицы 1 (износ протектора в мм)
# стандартная шина
standard = np.array(
    [
        3.099999905,
        4.099999905,
        4.699999809,
        4.800000191,
        4.300000191,
        4.699999809,
        4.599999905,
        3.599999905,
        6.000000000,
        3.599999905,
        4.199999809,
        4.900000095,
        4.300000191,
        5.699999809,
        3.200000048,
        3.799999952,
        5.000000000,
        4.500000000,
        5.800000191,
        5.300000191,
    ]
)

# новая шина
new = np.array(
    [
        2.900000095,
        2.599999905,
        5.000000000,
        3.700000048,
        4.599999905,
        4.099999905,
        3.099999905,
        4.599999905,
        5.300000191,
        3.500000000,
        4.300000191,
        5.099999905,
        3.099999905,
        5.099999905,
        3.799999952,
        2.299999952,
        4.000000000,
        3.900000095,
        4.099999905,
        3.700000048,
    ]
)

n_pairs = len(standard)
print(f"Количество грузовиков: n = {n_pairs}")
print()

# 1. Предварительный анализ данных
print("1. Предварительный анализ данных")
print()

print("Стандартная шина:")
print(f"Среднее значение: x_ст = {np.mean(standard):.3f} мм")
print(f"СКО: s_ст = {np.std(standard, ddof=1):.3f} мм")
print()

print("Новая шина:")
print(f"Среднее значение: x_н = {np.mean(new):.3f} мм")
print(f"СКО: s_н = {np.std(new, ddof=1):.3f} мм")
print()

# 2. Анализ парных разностей
print("2. Анализ парных разностей")
print()

differences = standard - new
d_mean = np.mean(differences)
d_std = np.std(differences, ddof=1)
d_std_sr = d_std / np.sqrt(n_pairs)

print("Статистические характеристики разностей:")
print(f"Среднее значение: d = {d_mean:.3f} мм")
print(f"СКО разностей: s_d = {d_std:.3f} мм")
print(f"СКО среднего: s_d = {d_std_sr:.3f} мм")
print()

# 3. Проверка гипотезы (t-критерий для зависимых выборок)
print("3. Проверка гипотезы о равенстве средних")
print()

t_stat = d_mean / d_std_sr  # t-статистика
df = n_pairs - 1  # число степеней свободы
t_critical = stats.t.ppf(0.975, df=df)  # квантиль
p_value = 2 * (1 - stats.t.cdf(abs(t_stat), df=df))  # двустороннее p-значение

print(f"t-статистика: t = {t_stat:.4f}")
print(f"Степени свободы: df = {df}")
print(f"Критическое значение (p = 0.95, двустороннее): t_кр = {t_critical:.4f}")
print(f"p-значение: p = {p_value:.6f}")
print()

# 4. Доверительный интервал для средней разности
print("4. Доверительный интервал для средней разности")
print()

ci_lower = d_mean - t_critical * d_std_sr
ci_upper = d_mean + t_critical * d_std_sr

print("95% доверительный интервал для средней разности:")
print(f"[{ci_lower:.3f}, {ci_upper:.3f}] мм")
print()

# 5. Выводы по результатам анализа
print("5. Выводы")
print()

print(f"Средняя разность износа (стандартная - новая) = {d_mean:.3f} мм")
print(f"доверительный интервал: [{ci_lower:.3f}, {ci_upper:.3f}] мм")

if abs(t_stat) > t_critical:
    print(f"|t| = {abs(t_stat):.4f} > t_кр = {t_critical:.4f}")
    print("Нулевая гипотеза отвергается на уровне значимости alpha = 0.05")
    if d_mean > 0:
        print("Износ стандартной шины статистически значимо больше износа новой шины")
    else:
        print("Износ новой шины статистически значимо больше износа стандартной шины")
else:
    print(f"|t| = {abs(t_stat):.4f} <= t_кр = {t_critical:.4f}")
    print("Нулевая гипотеза не отвергается на уровне значимости alpha = 0.05")
    print("Статистически значимой разницы в износе шин не обнаружено")

print()
print("Вывод:")
print("Это парный эксперимент, так как измерения проведены на одних и тех же объектах")
print("Парный эксперимент позволил исключить влияние различий между грузовиками")
