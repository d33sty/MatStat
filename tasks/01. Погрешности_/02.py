import numpy as np
import scipy.stats as stats

# ================ Дано ================
I = list(
    map(lambda x: x / 1000, [141, 144, 157, 147, 151, 165, 142, 150, 163, 161])
)  # ток в СИ в Амперах
R = [
    1.55,
    1.65,
    2.05,
    1.90,
    1.80,
    2.55,
    2.10,
    2.05,
    2.00,
    1.90,
]  # сопротивление в СИ в Омах

P_ot = 0.045  # предполагаемое теоретическое значение мощности
P_oe = 0.043  # ранее полученное экспериментальное значение мощности
deltaP_oe = 0.040  # абсолютная погрешность ранее полученного экспериментального значения мощности

# Классы точности
am_cl = 0.5
om_cl = 1

# Пределы шкалы
am_max = 0.200
om_max = 5


# ================================ Решение ================================
# ================ 1. Результаты прямых измерений тока и сопротивления ================
# Квантиль p = 0.95, n = 10
quantile = stats.t.ppf(0.975, df=9)

# Ток
I_mean = np.mean(I)  # Среднее
I_std = np.std(I, ddof=1)  # СКО*
I_n = len(I)  # n
I_std_sr = I_std / np.sqrt(I_n)  # СКО среднего
I_disp = I_std**2  # Дисперсия
deltaI_rand = quantile * I_std / np.sqrt(I_n)  # случайная
deltaI_inst = (am_cl * am_max) / 100  # инструментальная
deltaI = np.sqrt(deltaI_rand**2 + deltaI_inst**2)  # полная

# Сопротивление
R_mean = np.mean(R)  # Среднее
R_std = np.std(R, ddof=1)  # СКО*
R_n = len(R)  # n
R_std_sr = R_std / np.sqrt(R_n)  # СКО среднего
R_disp = R_std**2  # Дисперсия
deltaR_rand = quantile * R_std / np.sqrt(R_n)  # случайная
deltaR_inst = (om_cl * om_max) / 100  # инструментальная
deltaR = np.sqrt(deltaR_rand**2 + deltaR_inst**2)  # полная

# *ddof = 1, чтобы делить сумму квадратов отклонений на (n - ddof) = (n - 1)

print("\n" + "=" * 70)
print("1. Результаты прямых измерений тока и сопротивления")
print("=" * 70)
print(f"Ток:")
print(f"  Среднее значение: I = {I_mean*1000:.1f} мА")
print(f"  Статистические характеристики:")
print(f"    СКО: {I_std*1000:.2f} мА")
print(f"    Дисперсия: {I_disp*1e6:.2f} мА^2")
print(f"    СКО среднего: {I_std_sr*1000:.2f} мА")
print(f"  Погрешности (p = 0.95):")
print(f"    Случайная: deltaI_сл = {deltaI_rand*1000:.1f} мА")
print(f"    Инструментальная: deltaI_инст = {deltaI_inst*1000:.1f} мА")
print(f"    Полная: deltaI = {deltaI*1000:.1f} мА")
print(f"  Результат: I = ({I_mean*1000:.0f} ± {deltaI*1000:.0f}) мА, p = 0.95")
print()
print(f"Сопротивление:")
print(f"  Среднее значение: R = {R_mean:.3f} Ом")
print(f"  Статистические характеристики:")
print(f"    СКО: {R_std:.3f} Ом")
print(f"    Дисперсия: {R_disp:.4f} Ом^2")
print(f"    СКО среднего: {R_std_sr:.3f} Ом")
print(f"  Погрешности (p = 0.95):")
print(f"    Случайная: deltaR_сл = {deltaR_rand:.2f} Ом")
print(f"    Инструментальная: deltaR_инст = {deltaR_inst:.2f} Ом")
print(f"    Полная: deltaR = {deltaR:.2f} Ом")
print(f"  Результат: R = ({R_mean:.1f} ± {deltaR:.1f}) Ом, p = 0.95")


# ================ 2. Результаты косвенных измерений мощности ================

# Мощность
P = [I[i] ** 2 * R[i] for i in range(len(I))]  # P = I^2 * R

P_mean = np.mean(P)  # Среднее
P_std = np.std(P, ddof=1)  # СКО
P_disp = P_std**2  # Дисперсия
P_std_sr = P_std / np.sqrt(len(P))  # СКО среднего

# Производные
dP_dI = 2 * I_mean * R_mean
dP_dR = I_mean**2

# Полная погрешность
deltaP = np.sqrt((dP_dI * deltaI) ** 2 + (dP_dR * deltaR) ** 2)

print("\n" + "=" * 70)
print("2. Результаты косвенных измерений мощности")
print("=" * 70)
print(f"Статистические характеристики:")
print(f"  Среднее значение: P = {P_mean*1000:.2f} мВт")
print(f"  СКО: {P_std*1000:.2f} мВт")
print(f"  Дисперсия: {P_disp*1e6:.2f} мВт^2")
print(f"  СКО среднего: {P_std_sr*1000:.2f} мВт")
print()
print(f"Оценка погрешности")
print(f"  Частные производные в точке средних:")
print(f"    dP/dI = 2*I*R = {dP_dI:.4f} В")
print(f"    dP/dR = I^2 = {dP_dR:.6f} А^2")
print(f"  Составляющие погрешности:")
print(f"    (dP/dI)*deltaI = {dP_dI*deltaI*1000:.2f} мВт")
print(f"    (dP/dR)*deltaR = {dP_dR*deltaR*1000:.2f} мВт")
print(
    f"  Полная погрешность: deltaP = sqrt( (dP/dI*deltaI)^2 + (dP/dR*deltaR)^2 ) = {deltaP*1000:.1f} мВт"
)
print(f"  Относительная погрешность: delta = {deltaP/P_mean*100:.1f}%")
print(f"\nРезультат: P = ({P_mean*1000:.0f} ± {deltaP*1000:.0f}) мВт, p = 0.95")


# ================ 3. Сравнение с теоретическим значением ================

print("\n" + "=" * 70)
print("3. Сравнение с теоретическим значением")
print("=" * 70)
print(f"Теоретическое значение: P_ot = {P_ot*1000:.0f} мВт")
print(f"Измеренное значение: P = ({P_mean*1000:.0f} ± {deltaP*1000:.0f}) мВт, p = 0.95")

# Доверительный интервал
P_min = P_mean - deltaP
P_max = P_mean + deltaP

print(f"Доверительный интервал: [{P_min*1000:.0f}, {P_max*1000:.0f}] мВт")

# Проверка попадания теоретического значения в доверительный интервал
if P_min <= P_ot <= P_max:
    print(f"P_ot = {P_ot*1000:.0f} мВт попадает в доверительный интервал")
    print("  Результаты измерений СОГЛАСУЮТСЯ с теоретическим значением")
else:
    print(f"P_ot = {P_ot*1000:.0f} мВт НЕ попадает в доверительный интервал")
    print("  Результаты измерений НЕ СОГЛАСУЮТСЯ с теоретическим значением")


# ================ 4. Сравнение с предыдущим экспериментальным значением ================

print("\n" + "=" * 70)
print("4. Сравнение с предыдущим экспериментальным значением")
print("=" * 70)
print(
    f"Предыдущее измерение: P_oe = ({P_oe*1000:.0f} ± {deltaP_oe*1000:.0f}) мВт, p = 0.95"
)
print(
    f"Текущее измерение:    P = ({P_mean*1000:.0f} ± {deltaP*1000:.0f}) мВт, p = 0.95"
)

# Доверительный интервал ранее полученного экспериментальнгоо значения
P_oe_min = P_oe - deltaP_oe
P_oe_max = P_oe + deltaP_oe

print(
    f"Доверительный интервал текущего измерения: [{P_min*1000:.0f}, {P_max*1000:.0f}] мВт"
)
print(
    f"Доверительный интервал предыдущего измерения: [{P_oe_min*1000:.0f}, {P_oe_max*1000:.0f}] мВт"
)

# Проверка перекрытия доверительных интервалов
if (P_min <= P_oe_max) and (P_oe_min <= P_max):
    print(f"Доверительные интервалы ПЕРЕКРЫВАЮТСЯ")
    print("  Результаты измерений СОГЛАСУЮТСЯ с предыдущим экспериментальным значением")
else:
    print(f"Доверительные интервалы НЕ ПЕРЕКРЫВАЮТСЯ")
    print(
        "  Результаты измерений НЕ СОГЛАСУЮТСЯ с предыдущим экспериментальным значением"
    )


# ================ 5. Объединение результатов измерений мощности ================

print("\n" + "=" * 70)
print("5. Объединение результатов измерений мощности")
print("=" * 70)

# P1 - текущее измерение, P2 - предыдущее измерение
P1 = P_mean
deltaP1 = deltaP
P2 = P_oe
deltaP2 = deltaP_oe

print(f"Измерение 1 (текущее):     P1 = ({P1*1000:.0f} ± {deltaP1*1000:.0f}) мВт")
print(f"Измерение 2 (предыдущее):  P2 = ({P2*1000:.0f} ± {deltaP2*1000:.0f}) мВт")
print()

# Квадраты погрешностей
D1 = deltaP1**2
D2 = deltaP2**2

print(f"Квадраты погрешностей:")
print(f"  deltaP1^2 = {deltaP1**2*1e6:.0f} (мВт)^2")
print(f"  deltaP2^2 = {deltaP2**2*1e6:.0f} (мВт)^2")
print()

# Оптимальные весовые коэффициенты
# 1) k1 + k2 = 1, т.к. сохраняется матожидание
# 2) k1^2 * D1 + k2^2 * D2 -> min
# k1^2 * D1 + D2 - 2*k1*D2 - k1^2 * D2 -> min // Берем производную по k1
# 2*k1*D1 - 2*D2 - 2*k1*D2 = 0
# k1 = D2 / (D1 + D2)
# k2 = 1 - k1 = D1 / (D1 + D2)
# Квантиль сокращается
k1 = D2 / (D1 + D2)
k2 = D1 / (D1 + D2)

print(f"Оптимальные весовые коэффициенты:")
print(f"  k1 = deltaP2^2/(deltaP1^2+deltaP2^2) = {k1:.4f} (вес текущего измерения)")
print(f"  k2 = deltaP1^2/(deltaP1^2+deltaP2^2) = {k2:.4f} (вес предыдущего измерения)")
print()

# Объединенное значение мощности
P_combined = k1 * P1 + k2 * P2

print(f"Объединенное значение (взвешенное среднее):")
print(
    f"  P = k1*P1 + k2*P2 = {k1:.4f}*{P1*1000:.0f} + {k2:.4f}*{P2*1000:.0f} = {P_combined*1000:.1f} мВт"
)

# Погрешность
deltaP_combined = np.sqrt(k1**2 * deltaP1**2 + k2**2 * deltaP2**2)

print(f"\nПогрешность объединенного значения:")
print(
    f"  deltaP = sqrt(k1^2*deltaP1^2 + k2^2*deltaP2^2) = sqrt({k1**2:.4f}*{deltaP1**2*1e6:.0f} + {k2**2:.4f}*{deltaP2**2*1e6:.0f}) = {deltaP_combined*1000:.1f} мВт"
)
print(f"  Относительная погрешность: delta = {deltaP_combined/P_combined*100:.1f}%")
print()
print(
    f"РЕЗУЛЬТАТ ОБЪЕДИНЕНИЯ: P = ({P_combined*1000:.0f} ± {deltaP_combined*1000:.0f}) мВт, p = 0.95"
)
print("=" * 70)
